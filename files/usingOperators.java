//first time using operators

public class usingOperators {
    /**
     * @param args
     */
    public static void main(String[] args) {
        int a = 16;
        int b = 2;
        //using all operators
        System.out.println("a + b =" + (a + b));
        System.out.println("a - b =" + (a - b));
        System.out.println("a / b =" + (a / b));
        System.out.println("a % b =" + (a % b));
        System.out.println("a * b =" + (a * b));
        System.out.println("a > b =" + (a > b));
        System.out.println("a < b =" + (a < b));
        System.out.println("a == b =" + (a == b));
        System.out.println("a != b =" + (a != b));
        System.out.println("a increment =" + (++a));
        System.out.println("a decrement =" + (--a));
    }
    
}
